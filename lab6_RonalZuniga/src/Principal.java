    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        if (np1.getText().isEmpty() || launch1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos vacíos, no se ha agregado el programa");
        } else {
            Programa pr = (Programa) prg.getSelectedItem();
            String nombre = np1.getText();
            int pt = ((Integer) ptp1.getValue());
            int year = Integer.parseInt(launch1.getText());
            String tipo = type1.getSelectedItem().toString();
            String gen = genero1.getSelectedItem().toString();
            for (int i = 0; i < p.size(); i++) {
                if (p.get(i).equals(pr)) {
                    p.get(i).setNombre(nombre);
                    p.get(i).setPuntuacion(pt);
                    p.get(i).setLanzamiento(year);
                    p.get(i).setTipo(tipo);
                    p.get(i).setGenero(gen);
                }
            }
            JOptionPane.showMessageDialog(this, "Programa modificado correctamente");
            setProgramas();
            np1.setText("");
            launch1.setText("");
            ptp1.setValue(1);
        }
    }//GEN-LAST:event_jButton2MouseClicked

