    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if (np.getText().isEmpty() || launch.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campos vacíos, no se ha agregado el programa");
        } else {
            String nombre = np.getText();
            int pt = ((Integer) ptp.getValue());
            int year = Integer.parseInt(launch.getText());
            String tipo = type.getSelectedItem().toString();
            String gen = genero.getSelectedItem().toString();
            Programa pr = new Programa(nombre, pt, year, tipo, gen);
            p.add(pr);
            JOptionPane.showMessageDialog(this, "Programa agregaddo correctamente");
            setProgramas();
            np.setText("");
            launch.setText("");
            ptp.setValue(1);
        }
    }//GEN-LAST:event_jButton1MouseClicked

